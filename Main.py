

set1 = {1,2,3}
set2 = {4,5,6}
union_set = set1 | set2
print(union_set)
intersection_set = set1 & set2
print(intersection_set)
difference_set = set1 - set2
print(difference_set)
symmetric_difference_set = set1 ^ set2
print(symmetric_difference_set)

frozen_set = frozenset([1,2,3])
print(frozen_set)